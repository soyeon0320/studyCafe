package kr.co.scm.store.vo;

import java.util.Date;

public class StoreSalesVO {

	private Date day;
	private String dayByFormat;
	
	private int seatSales;
	private int studyroomSales;
	private int equipSales;
	
	public Date getDay() {
		return day;
	}
	public void setDay(Date day) {
		this.day = day;
	}
	public String getDayByFormat() {
		return dayByFormat;
	}
	public void setDayByFormat(String dayByFormat) {
		this.dayByFormat = dayByFormat;
	}
	public int getSeatSales() {
		return seatSales;
	}
	public void setSeatSales(int seatSales) {
		this.seatSales = seatSales;
	}
	public int getStudyroomSales() {
		return studyroomSales;
	}
	public void setStudyroomSales(int studyroomSales) {
		this.studyroomSales = studyroomSales;
	}
	public int getEquipSales() {
		return equipSales;
	}
	public void setEquipSales(int equipSales) {
		this.equipSales = equipSales;
	}
	
	
	
}
