package kr.co.scm.admin.vo;

import java.util.Date;

import kr.co.scm.base.vo.BaseVO;

public class NotitalkVO extends BaseVO{
	
	private int notitalkNo;
	private Date notitalkRegDate;
	private String adminId;
	private String notitalkContent;
	private String notitalkTitle;
	
	public int getNotitalkNo() {
		return notitalkNo;
	}
	public void setNotitalkNo(int notitalkNo) {
		this.notitalkNo = notitalkNo;
	}
	public Date getNotitalkRegDate() {
		return notitalkRegDate;
	}
	public void setNotitalkRegDate(Date notitalkRegDate) {
		this.notitalkRegDate = notitalkRegDate;
	}
	public String getAdminId() {
		return adminId;
	}
	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}
	public String getNotitalkContent() {
		return notitalkContent;
	}
	public void setNotitalkContent(String notitalkContent) {
		this.notitalkContent = notitalkContent;
	}
	public String getNotitalkTitle() {
		return notitalkTitle;
	}
	public void setNotitalkTitle(String notitalkTitle) {
		this.notitalkTitle = notitalkTitle;
	}
	
	
	
	

}
