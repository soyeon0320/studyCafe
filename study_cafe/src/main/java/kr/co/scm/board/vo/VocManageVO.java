package kr.co.scm.board.vo;

import java.util.Date;

import kr.co.scm.base.vo.BaseVO;

public class VocManageVO extends BaseVO{

	private int vocNo;
	private int vocReplyNo;
	private String vocReplyContent;
	private Date vocConfirmDate;
	private String adminId;
	private String replyModifyContent;
	
	private VocVO vocVO;
	private VocVO searchVocVO;
	
	public VocManageVO() {
		this.vocVO = new VocVO();
		this.searchVocVO = new VocVO();
	}
	
	
	public VocVO getSearchVocVO() {
		return searchVocVO;
	}
	public void setSearchVocVO(VocVO searchVocVO) {
		this.searchVocVO = searchVocVO;
	}
	public VocVO getVocVO() {
		return vocVO;
	}
	public void setVocVO(VocVO vocVO) {
		this.vocVO = vocVO;
	}
	public String getReplyModifyContent() {
		return replyModifyContent;
	}
	public void setReplyModifyContent(String replyModifyContent) {
		this.replyModifyContent = replyModifyContent;
	}
	public int getVocNo() {
		return vocNo;
	}
	public void setVocNo(int vocNo) {
		this.vocNo = vocNo;
	}
	public int getVocReplyNo() {
		return vocReplyNo;
	}
	public void setVocReplyNo(int vocReplyNo) {
		this.vocReplyNo = vocReplyNo;
	}
	public String getVocReplyContent() {
		return vocReplyContent;
	}
	public void setVocReplyContent(String vocReplyContent) {
		this.vocReplyContent = vocReplyContent;
	}
	public Date getVocConfirmDate() {
		return vocConfirmDate;
	}
	public void setVocConfirmDate(Date vocConfirmDate) {
		this.vocConfirmDate = vocConfirmDate;
	}
	public String getAdminId() {
		return adminId;
	}
	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}
	
	
	
	
	
	
	
	
}
