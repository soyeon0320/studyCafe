package kr.co.scm.admin.vo;

public class ContractViewFormVO {
	private ContractViewVO contractViewVO;
	private ContractViewVO searchContractVO;
	
	public ContractViewFormVO() {
		this.contractViewVO = new ContractViewVO();
		this.searchContractVO = new ContractViewVO();
	}

	public ContractViewVO getContractVO() {
		return contractViewVO;
	}

	public void setContractVO(ContractViewVO contractViewVO) {
		this.contractViewVO = contractViewVO;
	}

	public ContractViewVO getSearchContractVO() {
		return searchContractVO;
	}

	public void setSearchContractVO(ContractViewVO searchContractVO) {
		this.searchContractVO = searchContractVO;
	}
	
}
