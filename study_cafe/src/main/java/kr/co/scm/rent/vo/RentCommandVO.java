package kr.co.scm.rent.vo;

public class RentCommandVO {
	
	private String hour;
	private String checkInCnt;
	private String checkOutCnt;
	
	public String getHour() {
		return hour;
	}
	public void setHour(String hour) {
		this.hour = hour;
	}
	public String getCheckInCnt() {
		return checkInCnt;
	}
	public void setCheckInCnt(String checkInCnt) {
		this.checkInCnt = checkInCnt;
	}
	public String getCheckOutCnt() {
		return checkOutCnt;
	}
	public void setCheckOutCnt(String checkOutCnt) {
		this.checkOutCnt = checkOutCnt;
	}
	
	

}
