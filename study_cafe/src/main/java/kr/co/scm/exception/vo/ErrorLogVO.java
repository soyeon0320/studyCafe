package kr.co.scm.exception.vo;

import java.util.Date;

import kr.co.scm.base.vo.BaseVO;

public class ErrorLogVO extends BaseVO{
	private int errorLogNo;
	
	private Date errorRegDate;
	
	private String loginId;
	
	private String loginActor;
	
	private String requestIP;
	
	private String requestURI;
	
	private String params;
	
	private String cause;
	
	private String message;

	public Date getErrorRegDate() {
		return errorRegDate;
	}

	public String getLoginId() {
		return loginId;
	}

	public String getLoginActor() {
		return loginActor;
	}

	public String getRequestIP() {
		return requestIP;
	}

	public String getRequestURI() {
		return requestURI;
	}

	public String getParams() {
		return params;
	}

	public String getCause() {
		return cause;
	}

	public String getMessage() {
		return message;
	}

	public void setErrorRegDate(Date errorRegDate) {
		this.errorRegDate = errorRegDate;
	}

	public void setLoginId(String loginId) {
		this.loginId = loginId;
	}

	public void setLoginActor(String loginActor) {
		this.loginActor = loginActor;
	}

	public void setRequestIP(String requestIP) {
		this.requestIP = requestIP;
	}

	public void setRequestURI(String requestURI) {
		this.requestURI = requestURI;
	}

	public void setParams(String params) {
		this.params = params;
	}

	public void setCause(String cause) {
		this.cause = cause;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public int getErrorLogNo() {
		return errorLogNo;
	}

	public void setErrorLogNo(int errorLogNo) {
		this.errorLogNo = errorLogNo;
	}

	
	
}
