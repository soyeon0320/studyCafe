package kr.co.scm.store.vo;

import java.util.Date;
import java.util.List;

import org.springframework.format.annotation.DateTimeFormat;

import kr.co.scm.mypage.vo.ReviewVO;

public class SearchStoreVO extends StoreVO{
	private String ownerName;
	private String ownerEmail;
	private int studyroomCapa;
	
	@DateTimeFormat(pattern = "yyyy/MM/dd")
	private Date affiliateRegStartDate;
	@DateTimeFormat(pattern = "yyyy/MM/dd")
	private Date affiliateRegEndDate;
	
	private List<EquipVO> equipList;
	private List<ReviewVO> reviewList;
	
	
	
	
	
	public Date getAffiliateRegStartDate() {
		return affiliateRegStartDate;
	}
	public void setAffiliateRegStartDate(Date affiliateRegStartDate) {
		this.affiliateRegStartDate = affiliateRegStartDate;
	}
	public Date getAffiliateRegEndDate() {
		return affiliateRegEndDate;
	}
	public void setAffiliateRegEndDate(Date affiliateRegEndDate) {
		this.affiliateRegEndDate = affiliateRegEndDate;
	}
	public String getOwnerName() {
		return ownerName;
	}
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}
	public String getOwnerEmail() {
		return ownerEmail;
	}
	public void setOwnerEmail(String ownerEmail) {
		this.ownerEmail = ownerEmail;
	}
	public int getStudyroomCapa() {
		return studyroomCapa;
	}
	public void setStudyroomCapa(int studyroomCapa) {
		this.studyroomCapa = studyroomCapa;
	}
	public List<EquipVO> getEquipList() {
		return equipList;
	}
	public void setEquipList(List<EquipVO> equipList) {
		this.equipList = equipList;
	}
	public List<ReviewVO> getReviewList() {
		return reviewList;
	}
	public void setReviewList(List<ReviewVO> reviewList) {
		this.reviewList = reviewList;
	}
	
	
	
	
	
}
