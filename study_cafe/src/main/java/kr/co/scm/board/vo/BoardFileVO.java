package kr.co.scm.board.vo;

import java.util.Date;

import org.springframework.format.annotation.DateTimeFormat;

public class BoardFileVO {
	private int freeFileNo;
	private String freeFileName;
	private String freeFileOrigin;
	private String freeFileCours;
	private String freeFileExtsn;
	@DateTimeFormat(pattern="yyyy-MM-dd")
	private Date freeFileCreat;
	private int freeNo;
	
	public int getFreeFileNo() {
		return freeFileNo;
	}
	public void setFreeFileNo(int freeFileNo) {
		this.freeFileNo = freeFileNo;
	}
	public String getFreeFileName() {
		return freeFileName;
	}
	public void setFreeFileName(String freeFileName) {
		this.freeFileName = freeFileName;
	}
	public String getFreeFileOrigin() {
		return freeFileOrigin;
	}
	public void setFreeFileOrigin(String freeFileOrigin) {
		this.freeFileOrigin = freeFileOrigin;
	}
	public String getFreeFileCours() {
		return freeFileCours;
	}
	public void setFreeFileCours(String freeFileCours) {
		this.freeFileCours = freeFileCours;
	}
	public String getFreeFileExtsn() {
		return freeFileExtsn;
	}
	public void setFreeFileExtsn(String freeFileExtsn) {
		this.freeFileExtsn = freeFileExtsn;
	}
	public Date getFreeFileCreat() {
		return freeFileCreat;
	}
	public void setFreeFileCreat(Date freeFileCreat) {
		this.freeFileCreat = freeFileCreat;
	}
	public int getFreeNo() {
		return freeNo;
	}
	public void setFreeNo(int freeNo) {
		this.freeNo = freeNo;
	}
	
	
}
