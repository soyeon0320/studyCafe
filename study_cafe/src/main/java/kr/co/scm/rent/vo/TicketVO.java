package kr.co.scm.rent.vo;

import java.util.Date;

import kr.co.scm.base.vo.BaseVO;

public class TicketVO extends BaseVO{

	private int ticketNo;
	private int ticketPrice;
	private String ticketName;
	private String ticketCode;
	private String ticketStatus;
	private String ticketKind;
	private int ticketTime;
	private Date ticketTerm;
	
	
	public int getTicketNo() {
		return ticketNo;
	}
	public void setTicketNo(int ticketNo) {
		this.ticketNo = ticketNo;
	}
	public int getTicketPrice() {
		return ticketPrice;
	}
	public void setTicketPrice(int ticketPrice) {
		this.ticketPrice = ticketPrice;
	}
	public String getTicketName() {
		return ticketName;
	}
	public void setTicketName(String ticketName) {
		this.ticketName = ticketName;
	}
	public String getTicketCode() {
		return ticketCode;
	}
	public void setTicketCode(String ticketCode) {
		this.ticketCode = ticketCode;
	}
	public String getTicketStatus() {
		return ticketStatus;
	}
	public void setTicketStatus(String ticketStatus) {
		this.ticketStatus = ticketStatus;
	}
	public String getTicketKind() {
		return ticketKind;
	}
	public void setTicketKind(String ticketKind) {
		this.ticketKind = ticketKind;
	}
	public int getTicketTime() {
		return ticketTime;
	}
	public void setTicketTime(int ticketTime) {
		this.ticketTime = ticketTime;
	}
	public Date getTicketTerm() {
		return ticketTerm;
	}
	public void setTicketTerm(Date ticketTerm) {
		this.ticketTerm = ticketTerm;
	}
	
	
	
}
