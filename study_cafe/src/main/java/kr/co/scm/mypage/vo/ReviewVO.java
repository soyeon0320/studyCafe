package kr.co.scm.mypage.vo;

import java.text.SimpleDateFormat;
import java.util.Date;

import org.springframework.format.annotation.DateTimeFormat;

import kr.co.scm.base.vo.BaseVO;

public class ReviewVO extends BaseVO {

	private String memId;
	private int reviewNo;
	private String storeCode;
	private String kind;
	private float grade;
	@DateTimeFormat(pattern="yyyy/MM/dd")
	private Date reivewRegDate;
	private String storeName;
	private String reviewContent;
	private String formatRegDate;
	
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public int getReviewNo() {
		return reviewNo;
	}
	public void setReviewNo(int reviewNo) {
		this.reviewNo = reviewNo;
	}
	public String getStoreCode() {
		return storeCode;
	}
	public void setStoreCode(String storeCode) {
		this.storeCode = storeCode;
	}
	public String getKind() {
		return kind;
	}
	public void setKind(String kind) {
		this.kind = kind;
	}
	public float getGrade() {
		return grade;
	}
	public void setGrade(float grade) {
		this.grade = grade;
	}
	public Date getReivewRegDate() {
		return reivewRegDate;
	}
	public void setReivewRegDate(Date reivewRegDate) {
		this.reivewRegDate = reivewRegDate;
	}
	public String getStoreName() {
		return storeName;
	}
	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}
	public String getReviewContent() {
		return reviewContent;
	}
	public void setReviewContent(String reviewContent) {
		this.reviewContent = reviewContent;
	}
	public String getFormatRegDate() {
		return formatRegDate;
	}
	public void setFormatRegDate(String formatRegDate) {
		this.formatRegDate = formatRegDate;
	}
	
	

	
}
