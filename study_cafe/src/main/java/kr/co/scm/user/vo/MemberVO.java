package kr.co.scm.user.vo;

import java.util.Date;

import kr.co.scm.base.vo.BaseVO;
import kr.co.scm.common.vo.UseInfoVO;

public class MemberVO extends BaseVO{
	
	private String memId;
	private String memName;
	private String memPass;
	private String memTel;
	private String memEmail;
	private String memBir;
	private String memDeleted;
	private String memRoute;
	private String memGen;
	private String memMemo;
	private Date memRegDate;
	
	private String storeCode;
	private int seatTotaltime;
	

	private UseInfoVO useInfoVO;
	
	
	public MemberVO() {	}

	public MemberVO(String memId) {
		this.memId = memId;
	}
	
	
	
	public Date getMemRegDate() {
		return memRegDate;
	}
	public void setMemRegDate(Date memRegDate) {
		this.memRegDate = memRegDate;
	}
	public int getSeatTotaltime() {
		return seatTotaltime;
	}
	public void setSeatTotaltime(int seatTotaltime) {
		this.seatTotaltime = seatTotaltime;
	}
	public String getStoreCode() {
		return storeCode;
	}
	public void setStoreCode(String storeCode) {
		this.storeCode = storeCode;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getMemName() {
		return memName;
	}
	public void setMemName(String memName) {
		this.memName = memName;
	}
	public String getMemPass() {
		return memPass;
	}



	public void setMemPass(String memPass) {
		this.memPass = memPass;
	}



	public String getMemTel() {
		return memTel;
	}



	public void setMemTel(String memTel) {
		this.memTel = memTel;
	}



	public String getMemEmail() {
		return memEmail;
	}



	public void setMemEmail(String memEmail) {
		this.memEmail = memEmail;
	}



	public String getMemBir() {
		return memBir;
	}



	public void setMemBir(String memBir) {
		this.memBir = memBir;
	}



	public String getMemDeleted() {
		return memDeleted;
	}



	public void setMemDeleted(String memDeleted) {
		this.memDeleted = memDeleted;
	}



	public String getMemRoute() {
		return memRoute;
	}



	public void setMemRoute(String memRoute) {
		this.memRoute = memRoute;
	}



	public String getMemGen() {
		return memGen;
	}



	public void setMemGen(String memGen) {
		this.memGen = memGen;
	}



	public String getMemMemo() {
		return memMemo;
	}


	public void setMemMemo(String memMemo) {
		this.memMemo = memMemo;
	}
	public UseInfoVO getUseInfoVO() {
		return useInfoVO;
	}
	public void setUseInfoVO(UseInfoVO useInfoVO) {
		this.useInfoVO = useInfoVO;
	}
	
}
